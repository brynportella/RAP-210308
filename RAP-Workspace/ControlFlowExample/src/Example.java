
public class Example {
	public static void main(String[] args) {
		//Create variables of the different primitive datatypes
		//Define/Declare
		boolean bob; 
		
		byte jane; 
		//initialize 
		bob = true; 
		jane = 34; 
		
		//8 bits
		byte byteVariable = 9; 
		//16 bits
		short shortVariable = 68; 
		//32 bits
		int intVariable = -100; 
		//64 bits
		long longVariable = 45L;
		
		//char datatype 
		char charVariable = '9'; 
		charVariable = 120; 
		
		System.out.println(charVariable);
		//64 bits
		double doubleVar = 16.4;
		//32 bit 
		float floatVar = 8.9f; 
	}
}
